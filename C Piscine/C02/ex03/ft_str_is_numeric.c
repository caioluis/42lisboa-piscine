int	ft_str_is_numeric(char *str)
{
	int	i;
	int	value;

	i = 0;
	value = 1;
	while (str[i] != '\0')
	{
		if (str[i] < 48 || str[i] > 57)
			value = 0;
		i++;
	}
	return (value);
}
